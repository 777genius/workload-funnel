import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath, URL } from "node:url";

const repositoryRoot = fileURLToPath(new URL("../../", import.meta.url));
const sourcePath = new URL("./composition.source.json", import.meta.url);
const checkOnly = process.argv.includes("--check");

const source = JSON.parse(await readFile(sourcePath, "utf8"));
const failures = [];
const allowedCapabilities = new Set([
  "artifact_retention_deletion",
  "artifact_verification",
  "bounded_capacity_reservation",
  "deterministic_systemd_process_ownership",
  "external_scheduler_dispatch",
  "foreground_runtime_ownership",
  "hyperqueue_ambiguous_submit_reconciliation",
  "local_dispatch",
  "pinned_execution_paths",
  "postgres_atomic_acceptance",
  "pressure_fail_closed_admission",
  "provider_runtime_execution",
]);
const expectedProfileIds = ["control-postgres", "control-sqlite"];

if (source.manifestVersion !== 1 || !Array.isArray(source.profiles)) {
  throw new Error("Unsupported application-composition source schema");
}
if (
  Object.keys(source).sort().join() !== "manifestVersion,profiles" ||
  source.profiles.map((profile) => profile.profileId).join() !==
    expectedProfileIds.join()
) {
  throw new Error(
    "Application-composition source has unknown fields or profiles",
  );
}

for (const profile of source.profiles) {
  if (
    Object.keys(profile).sort().join() !== "availableCapabilities,profileId" ||
    !Array.isArray(profile.availableCapabilities) ||
    profile.availableCapabilities.some(
      (capability) => !allowedCapabilities.has(capability),
    ) ||
    [...new Set(profile.availableCapabilities)].sort().join() !==
      profile.availableCapabilities.join()
  ) {
    throw new Error(`Invalid composition profile ${String(profile.profileId)}`);
  }

  const outputPath = new URL(
    `../../apps/control-service/src/generated/composition.${profile.profileId}.ts`,
    import.meta.url,
  );
  const capabilities =
    profile.availableCapabilities.length === 1
      ? `[${JSON.stringify(profile.availableCapabilities[0])}]`
      : `[\n${profile.availableCapabilities
          .map((capability) => `    ${JSON.stringify(capability)},`)
          .join("\n")}\n  ]`;
  const sqliteProfile = profile.profileId === "control-sqlite";
  const artifactImport = sqliteProfile
    ? 'import { createDisposableSynchronousArtifactWriter } from "@workload-funnel/artifact-store-filesystem/verify-finalize";\n'
    : "";
  const databaseConstruction = sqliteProfile
    ? `createSyntheticDatabase(
      "sqlite",
      createDisposableSynchronousArtifactWriter(),
    )`
    : `createSyntheticDatabase("postgres")`;
  const rendered = `// Generated by tooling/architecture/generate-composition.mjs. Do not edit.
${artifactImport}import {
  createPhase1SyntheticService,
  createSyntheticDatabase,
} from "@workload-funnel/control-service/workload-controller";
import { createCapabilityRequirementEvaluator } from "@workload-funnel/workload-control/tenant-admission";

export const profileId = ${JSON.stringify(profile.profileId)} as const;

export const evaluateCapabilityRequirements =
  createCapabilityRequirementEvaluator(${capabilities});

export function createControlService() {
  const phase1 = createPhase1SyntheticService(
    ${databaseConstruction},
  );
  return Object.freeze({ evaluateCapabilityRequirements, phase1, profileId });
}
`;

  if (checkOnly) {
    const current = await readFile(outputPath, "utf8").catch(() => "");
    if (current !== rendered) {
      failures.push(
        `${fileURLToPath(outputPath).slice(repositoryRoot.length)} is stale; run pnpm generate`,
      );
    }
  } else {
    await writeFile(outputPath, rendered);
  }
}

if (failures.length > 0) {
  throw new Error(failures.join("\n"));
}

console.log(
  checkOnly
    ? `Composition generation check passed (${source.profiles.length} profiles)`
    : `Generated ${source.profiles.length} composition profiles`,
);
