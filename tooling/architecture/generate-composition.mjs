import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath, URL } from "node:url";

import { format } from "prettier";

const repositoryRoot = fileURLToPath(new URL("../../", import.meta.url));
const sourcePath = new URL("./composition.source.json", import.meta.url);
const checkOnly = process.argv.includes("--check");

const source = JSON.parse(await readFile(sourcePath, "utf8"));
const failures = [];
const allowedCapabilities = new Set([
  "artifact_retention_deletion",
  "artifact_verification",
  "bounded_capacity_reservation",
  "deterministic_systemd_process_ownership",
  "external_scheduler_dispatch",
  "foreground_runtime_ownership",
  "hyperqueue_ambiguous_submit_reconciliation",
  "local_dispatch",
  "pinned_execution_paths",
  "postgres_atomic_acceptance",
  "pressure_fail_closed_admission",
  "provider_runtime_execution",
]);
const expectedProfileIds = ["control-postgres", "control-sqlite"];

if (source.manifestVersion !== 1 || !Array.isArray(source.profiles)) {
  throw new Error("Unsupported application-composition source schema");
}
if (
  Object.keys(source).sort().join() !== "manifestVersion,profiles" ||
  source.profiles.map((profile) => profile.profileId).join() !==
    expectedProfileIds.join()
) {
  throw new Error(
    "Application-composition source has unknown fields or profiles",
  );
}

for (const profile of source.profiles) {
  if (
    Object.keys(profile).sort().join() !== "availableCapabilities,profileId" ||
    !Array.isArray(profile.availableCapabilities) ||
    profile.availableCapabilities.some(
      (capability) => !allowedCapabilities.has(capability),
    ) ||
    [...new Set(profile.availableCapabilities)].sort().join() !==
      profile.availableCapabilities.join()
  ) {
    throw new Error(`Invalid composition profile ${String(profile.profileId)}`);
  }

  const outputPath = new URL(
    `../../apps/control-service/src/generated/composition.${profile.profileId}.ts`,
    import.meta.url,
  );
  const capabilities =
    profile.availableCapabilities.length === 1
      ? `[${JSON.stringify(profile.availableCapabilities[0])}]`
      : `[\n${profile.availableCapabilities
          .map((capability) => `    ${JSON.stringify(capability)},`)
          .join("\n")}\n  ]`;
  const sqliteProfile = profile.profileId === "control-sqlite";
  const artifactImport = sqliteProfile
    ? 'import { createDisposableSynchronousArtifactWriter } from "@workload-funnel/artifact-store-filesystem/verify-finalize";\n'
    : "";
  const databaseConstruction = `createSyntheticDatabase(
      "sqlite",
      createDisposableSynchronousArtifactWriter(),
    )`;
  const sqliteSource = `// Generated by tooling/architecture/generate-composition.mjs. Do not edit.
${artifactImport}import type { TransportIdentityBinding } from "@workload-funnel/control-service/authentication";
import { createTransportAuthenticator } from "@workload-funnel/control-service/authentication";
import type { PrincipalAuthorizationPolicy } from "@workload-funnel/control-service/authorization";
import { createAuthorizationService } from "@workload-funnel/control-service/authorization";
import { createCapacityController } from "@workload-funnel/control-service/node-controller";
import {
  createPhase1SyntheticService,
  createPhase5SyntheticPublicOperations,
  createSyntheticDatabase,
} from "@workload-funnel/control-service/workload-controller";
import { createReconciliationController } from "@workload-funnel/control-service/reconciliation-controller";
import { createResultOperationsController } from "@workload-funnel/control-service/result-controller";
import type { CursorKeyset } from "@workload-funnel/control-service/transport-http";
import { createPublicHttpApi, createSignedCursorCodec } from "@workload-funnel/control-service/transport-http";
import { createPublicWorkloadController } from "@workload-funnel/control-service/workload-controller";
import type { RedactionPolicy } from "@workload-funnel/observability/redaction-policy";
import { createStructuredRedactor } from "@workload-funnel/observability/redaction-policy";
import type { DependencyHealth, TelemetrySink } from "@workload-funnel/observability/telemetry-export";
import { createServiceOperationsAdapter, createTelemetryProvider, evaluateServiceHealth } from "@workload-funnel/observability/telemetry-export";
import { createCapabilityRequirementEvaluator } from "@workload-funnel/workload-control/tenant-admission";

export const profileId = ${JSON.stringify(profile.profileId)} as const;

export interface PublicApiCompositionOptions {
  readonly identityBindings: readonly TransportIdentityBinding[];
  readonly authorizationPolicies: readonly PrincipalAuthorizationPolicy[];
  readonly cursorKeyset: CursorKeyset;
  readonly cursorTtlMs: number;
  readonly telemetrySink: TelemetrySink;
  readonly redactionPolicy: RedactionPolicy;
  readonly health: () => DependencyHealth;
  readonly clock?: () => number;
}

export const evaluateCapabilityRequirements =
  createCapabilityRequirementEvaluator(${capabilities});

export function createControlService() {
  const database = ${databaseConstruction};
  const phase1 = createPhase1SyntheticService(database);

  function createPublicApi(options: PublicApiCompositionOptions) {
    const clock = options.clock ?? Date.now;
    const operations = createPhase5SyntheticPublicOperations(
      phase1,
      database,
      clock,
    );
    const telemetry = createTelemetryProvider(
      options.telemetrySink,
      createStructuredRedactor(options.redactionPolicy),
    );
    return createPublicHttpApi({
      authenticator: createTransportAuthenticator(options.identityBindings),
      authorization: createAuthorizationService(options.authorizationPolicies),
      capacity: createCapacityController(operations.capacity),
      clock,
      cursorCodec: createSignedCursorCodec(
        options.cursorKeyset,
        options.cursorTtlMs,
      ),
      events: operations.events,
      reconciliation: createReconciliationController(operations.reconciliation),
      results: createResultOperationsController(operations.results),
      serviceOperations: createServiceOperationsAdapter({
        clock,
        domainSnapshot: () => ({
          allocations: Object.freeze([...database.state.allocations.values()]),
          cancellations: Object.freeze([...database.state.sagas.values()]),
          capacity: Object.freeze([]),
          dispatches: Object.freeze([...database.state.dispatches.values()]),
          executions: Object.freeze([...database.state.executions.values()]),
          gates: Object.freeze([database.state.gateSet]),
          namespaceOwnership: Object.freeze([]),
          nodes: Object.freeze([]),
          results: Object.freeze([...database.state.manifests.values()]),
          workloads: Object.freeze(
            [...database.state.runById.keys()]
              .map((runId) => phase1.status(runId))
              .filter(
                (item): item is NonNullable<ReturnType<typeof phase1.status>> =>
                  item !== undefined,
              ),
          ),
        }),
        health: () => evaluateServiceHealth(options.health()),
        telemetry,
      }),
      workloads: createPublicWorkloadController(operations.workloads),
    });
  }

  return Object.freeze({
    createPublicApi,
    evaluateCapabilityRequirements,
    phase1,
    profileId,
  });
}
`;
  const postgresSource = `// Generated by tooling/architecture/generate-composition.mjs. Do not edit.
import {
  createPostgresLifecycleDatabase,
  type PostgresLifecycleProductionConfig,
} from "@workload-funnel/store-postgres/workload-persistence";
import { migratePostgresLifecycleSchema } from "@workload-funnel/store-postgres/schema-migrations";
import { createCapabilityRequirementEvaluator } from "@workload-funnel/workload-control/tenant-admission";
import { createAsyncWorkloadLifecycleService } from "@workload-funnel/workload-control/workload-lifecycle";

export const profileId = "control-postgres" as const;
export const productionStartsEnabled = false as const;

export interface PostgresControlServiceOptions {
  readonly database: PostgresLifecycleProductionConfig;
}

export const evaluateCapabilityRequirements =
  createCapabilityRequirementEvaluator(${capabilities});

export async function createControlService(
  options: PostgresControlServiceOptions,
) {
  const database = createPostgresLifecycleDatabase({
    config: options.database,
  });
  try {
    await migratePostgresLifecycleSchema({
      executor: database.migrationExecutor,
      owner: database.schemaOwner,
      schema: database.schema,
    });
    return Object.freeze({
      close: () => database.close(),
      evaluateCapabilityRequirements,
      lifecycle: createAsyncWorkloadLifecycleService(database.repository),
      productionStartsEnabled,
      profileId,
    });
  } catch (error) {
    await database.close();
    throw error;
  }
}

export function startControlService(): never {
  throw new Error("production_starts_disabled");
}
`;
  const rendered = await format(sqliteProfile ? sqliteSource : postgresSource, {
    parser: "typescript",
  });

  if (checkOnly) {
    const current = await readFile(outputPath, "utf8").catch(() => "");
    if (current !== rendered) {
      failures.push(
        `${fileURLToPath(outputPath).slice(repositoryRoot.length)} is stale; run pnpm generate`,
      );
    }
  } else {
    await writeFile(outputPath, rendered);
  }
}

if (failures.length > 0) {
  throw new Error(failures.join("\n"));
}

console.log(
  checkOnly
    ? `Composition generation check passed (${source.profiles.length} profiles)`
    : `Generated ${source.profiles.length} composition profiles`,
);
