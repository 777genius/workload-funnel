export async function loadControlServiceOptions(
  path: string,
): Promise<PostgresControlServiceOptions> {
  if (!isAbsolute(path) || path.includes("\\0"))
    throw new Error("production_config_path_invalid");
  const handle = await open(
    path,
    fileConstants.O_RDONLY | fileConstants.O_NOFOLLOW,
  );
  try {
    const metadata = await handle.stat();
    if (
      !metadata.isFile() ||
      metadata.uid !== process.getuid?.() ||
      metadata.size < 2 ||
      metadata.size > 2_097_152 ||
      (metadata.mode & 0o077) !== 0
    )
      throw new Error("production_config_file_unsafe");
    const parsed = JSON.parse(await handle.readFile("utf8")) as unknown;
    if (
      typeof parsed !== "object" ||
      parsed === null ||
      Array.isArray(parsed) ||
      Object.keys(parsed).length !== 2 ||
      !Object.hasOwn(parsed, "database") ||
      !Object.hasOwn(parsed, "server")
    )
      throw new Error("production_config_file_invalid");
    const record = parsed as Record<string, unknown>;
    return Object.freeze({
      database: record["database"],
      server: record["server"],
    });
  } finally {
    await handle.close();
  }
}

